export function GetRandomQuestion(): Question {
    const out = Array.from(questionsData);
    for (let i = out.length - 1; i > 0; i--) {
        const r = Math.floor(Math.random() * (i + 1));
        const tmp = out[i];
        out[i] = out[r];
        out[r] = tmp;
      }
    return out[0]
}
